import 'package:flutter/material.dart';
import 'dart:math' as math;

class Baader221CustomPainter extends CustomPainter {
  final Color color;
  final double strokeWidth;

  Baader221CustomPainter({
    this.color = Colors.blue,
    this.strokeWidth = 2.0,
  });

  @override
  void paint(Canvas canvas, Size size) {
    final paint = Paint()
      ..color = color
      ..strokeWidth = strokeWidth
      ..style = PaintingStyle.stroke;

    final path = buildMachinePath(size);

    final bounds = path.getBounds();
    final scaleX = size.width / bounds.width;
    final scaleY = size.height / bounds.height;
    final scale = math.min(scaleX, scaleY) * 0.9;

    // center of the path in its own coords
    final cx = bounds.left + bounds.width / 2;
    final cy = bounds.top + bounds.height / 2;

    canvas.save();

    // translate to canvas center first
    canvas.translate(size.width / 2, size.height / 2);
    canvas.rotate(math.pi);

    // apply flip by making x-scale negative
    final flipHorizontally = true;
    final sx = (flipHorizontally ? -scale : scale);
    canvas.scale(sx, scale);

    // move path center to origin (opposite direction because we already moved to canvas center)
    canvas.translate(-cx, -cy);

    // adjust stroke width
    paint.strokeWidth = strokeWidth / scale.abs();

    canvas.drawPath(path, paint);
    canvas.restore();
  }

  @override
  bool shouldRepaint(CustomPainter oldDelegate) {
    return oldDelegate is Baader221CustomPainter &&
        (oldDelegate.color != color || oldDelegate.strokeWidth != strokeWidth);
  }
}

